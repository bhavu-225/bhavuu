#typecasting
'''a has integer value 10, variable b,c,d,e,f,g and h 
will perform float,complex,string,list,set,dictionary and tuple function respectively
and also displays data type of same'''

a = 10
b = float(a)
print(type(b))
c = complex(a)
print(type(c))
d = str(a)
print(type(d))
e = [a]
print(type(e))
f = {a}
#a = f
print(a)
g = {vars: a}
print(type(g))
h = (1,2,3)
print(type(h))
print(range(0,a))



